  <h1>About Me</h1>
  <p>
    I am a <strong>Non Dualistic teacher</strong> based in the ancient wisdom tradition of <strong>Advaita Vedanta</strong>. My work is dedicated to guiding others toward the direct recognition of their true nature—the understanding that the apparent separation between the individual self and the absolute is an illusion.
  </p>
  <p>
    Advaita Vedanta, which means "non-duality" or "not two," points to the fundamental truth that there is only one reality—<strong>Brahman</strong>—and that the individual self (<strong>Atman</strong>) is not separate from this ultimate reality. Through direct inquiry, self-investigation, and the timeless teachings of this tradition, I help students see through the veil of identification with the body, mind, and personal story.
  </p>
  <p>
    My approach is rooted in the classical methods of Advaita Vedanta, drawing from the profound insights of teachers like <strong>Adi Shankara</strong>, <strong>Ramana Maharshi</strong>, and the <strong>Upanishads</strong>. I emphasize direct pointing, self-inquiry (<strong>Atma Vichara</strong>), and the recognition that liberation is not something to be attained, but rather something to be realized as already present.
  </p>

  <h2>Teaching Approach</h2>
  <p>
    My teaching focuses on:
  </p>
  <ul>
    <li>Direct pointing to the nature of awareness and consciousness.</li>
    <li>Self-inquiry practices to investigate the question "Who am I?"</li>
    <li>Understanding the three states of consciousness (waking, dreaming, deep sleep) and the witness that observes them.</li>
    <li>Recognizing the illusory nature of the ego and personal identity.</li>
    <li>Integration of this understanding into daily life.</li>
  </ul>
