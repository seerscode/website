  <h1>About Me</h1>
  <p>
    I am a researcher and engineer transitioning from building decentralized systems to the field of AI safety, with a deep focus on <strong>mechanistic interpretability</strong> (MechInterp). This move is a natural evolution of my core technical interests.
  </p>
  <p>
    My academic journey has been dedicated to the formal understanding of computational systems. I hold a <strong>Master's degree in Computer Science</strong>, where my thesis explored <strong>modal logics</strong>, followed by a PhD in the <strong>formal verification of programs</strong> focused on creating mathematical proofs of software correctness. I see a powerful parallel between ensuring a traditional program is safe and the central challenge of MechInterp: reverse-engineering the internal 'circuits' of neural networks to verify their behavior.
  </p>
  <p>
    Previously, I applied these principles of security and rigor at a massive scale while working on the core infrastructure of <strong>Polkadot</strong>. There, I had the privilege of working alongside Ethereum co-founder <strong>Gavin Wood</strong> to build one of the world's most complex blockchain protocols. That experience in deconstructing and securing large-scale systems directly informs my approach to understanding the intricate architectures of modern AI models.
  </p>

  <h2>Research Interests</h2>
  <p>
    My primary interest lies in identifying and understanding the 'circuits' within transformer models responsible for specific behaviors. I am particularly interested in:
  </p>
  <ul>
    <li>Induction heads and the mechanisms of in-context learning.</li>
    <li>Techniques for automated circuit discovery and verification.</li>
    <li>Superposition and the challenges of polysemantic neurons.</li>
  </ul>
