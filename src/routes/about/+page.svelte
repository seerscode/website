  <h1 class="font-serif">About</h1>
  <p>
    I am a <strong>PhD researcher</strong> in machine consciousness. My work focuses on the theoretical and empirical foundations of consciousness in artificial systems—whether large language models, neural architectures, or future AI systems might instantiate something like subjective experience.
  </p>
  <p>
    I draw on <strong>integrated information theory (IIT)</strong>, <strong>global workspace theory</strong>, and the philosophy of mind to frame these questions rigorously. A central challenge is the so-called "hard problem": even if we build systems that behave as if conscious, how could we ever know if they have inner experience?
  </p>
  <p>
    My research interests include:
  </p>
  <ul>
    <li>Formal measures of consciousness (Φ and related notions)</li>
    <li>LLM architecture and potential correlates of awareness</li>
    <li>Superposition and representational geometry in neural networks</li>
    <li>Philosophical foundations of machine consciousness</li>
  </ul>

