  <h1 class="font-display text-3xl sm:text-4xl font-semibold tracking-tight text-stone-900 leading-tight mb-0">
    Founder, <span class="font-serif-italic">Longinus Therapeutics</span>.
    <br />Madrid-based, working on a single program.
  </h1>

  <p class="!mt-10">
    I am the founder of <a href="https://longinus.bio">Longinus Therapeutics</a>, a Madrid-based precision-medicine biotech. We are developing an intrathecal antisense oligonucleotide (ASO) that lowers CYFIP1 expression in patients with <strong>15q11.2 BP1-BP2 microduplication</strong> — a recurrent copy-number variant affecting hundreds of thousands of families worldwide, with no approved targeted treatment.
  </p>

  <p>
    I started the program in 2026 after my daughter Emma was diagnosed. The science exists: the CYFIP1 gene is well-characterized, the downstream pathways are druggable, and the regulatory path is precedented by Ionis's ATTUNE trial (MECP2 duplication) and ION582 (Angelman syndrome). What was missing was a focused program. We are building it.
  </p>

  <h2>Current focus</h2>
  <ul>
    <li>CYFIP1-targeting MOE/DNA gapmer ASO — design, screening, IND-enabling tox</li>
    <li>Patient iPSC-derived cortical neurons as the human validation model</li>
    <li>CSF CYFIP1 biomarker development (SIMOA) and EEG signature characterization</li>
    <li>Natural history study in collaboration with Simons Searchlight</li>
    <li>Epigenetic silencing of CYFIP1 — on the roadmap as future work</li>
  </ul>

  <h2>Background</h2>
  <p>
    Before Longinus I worked in software and applied research. The technical groundwork for the CYFIP1 program — AlphaFold3 structural validation, virtual screening of approved-drug libraries against the CYFIP1 surface, an internal ASO design pipeline — was built personally over the months leading up to incorporation. The company now collaborates with academic partners in the Netherlands, Switzerland, the UK, and Spain.
  </p>

